CREATE TABLE POLICY_PLAN (
    ID              NUMBER GENERATED BY DEFAULT AS IDENTITY,
    POLICY_NO       VARCHAR2(50)  NOT NULL,
    ORDER_NO        NUMBER(2)     NOT NULL,
    PLAN_CODE       VARCHAR2(10)  NOT NULL,
    INSURED_ID      NUMBER,
    HOLDER_ID       NUMBER,
    EFFECTIVE_DATE  DATE          NOT NULL,
    STATUS          VARCHAR2(1)   NOT NULL,
    PREMIUM         NUMBER(20, 2) NOT NULL,
    SUM_ASSURED     NUMBER(20, 2),
    UNIT            NUMBER(20),
    PAYMENT_PERIOD   NUMBER(3),
    COVERAGE_PERIOD NUMBER(3),
    CREATED_BY      VARCHAR2(20) DEFAULT USER,
    CREATED_AT      TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    UPDATED_BY      VARCHAR2(20) DEFAULT USER,
    UPDATED_AT      TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_POLICY_PLAN PRIMARY KEY (ID)
);

CREATE UNIQUE INDEX UK_IDX_POLICY_PLAN ON POLICY_PLAN (POLICY_NO, ORDER_NO);

COMMENT ON TABLE POLICY_PLAN IS '保单险种明细表';
COMMENT ON COLUMN POLICY_PLAN.ID IS '主键ID';
COMMENT ON COLUMN POLICY_PLAN.POLICY_NO IS '保单号';
COMMENT ON COLUMN POLICY_PLAN.ORDER_NO IS '主附约号';
COMMENT ON COLUMN POLICY_PLAN.PLAN_CODE IS '险种代码';
COMMENT ON COLUMN POLICY_PLAN.INSURED_ID IS '被保人ID';
COMMENT ON COLUMN POLICY_PLAN.HOLDER_ID IS '投保人ID';
COMMENT ON COLUMN POLICY_PLAN.EFFECTIVE_DATE IS '险种生效时间';
COMMENT ON COLUMN POLICY_PLAN.STATUS IS '险种状态';
COMMENT ON COLUMN POLICY_PLAN.PREMIUM IS '保费';
COMMENT ON COLUMN POLICY_PLAN.SUM_ASSURED IS '保额';
COMMENT ON COLUMN POLICY_PLAN.UNIT IS '份数';
COMMENT ON COLUMN POLICY_PLAN.PAYMENT_PERIOD IS '缴费年期';
COMMENT ON COLUMN POLICY_PLAN.COVERAGE_PERIOD IS '保障年期';
COMMENT ON COLUMN POLICY_PLAN.CREATED_BY IS '创建人';
COMMENT ON COLUMN POLICY_PLAN.CREATED_AT IS '创建时间';
COMMENT ON COLUMN POLICY_PLAN.UPDATED_BY IS '更新人';
COMMENT ON COLUMN POLICY_PLAN.UPDATED_AT IS '更新时间';
