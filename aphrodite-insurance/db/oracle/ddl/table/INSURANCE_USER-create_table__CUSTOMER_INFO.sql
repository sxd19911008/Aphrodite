CREATE TABLE CUSTOMER_INFO
(
    -- 自增，如果insert语句中传入ID，则采用传入的值。有传入ID的操作后，查询当前最大id然后设置正确的计数器
    ID            NUMBER GENERATED BY DEFAULT AS IDENTITY,
    CUSTOMER_NAME VARCHAR2(100)          NOT NULL,
    GENDER        VARCHAR2(1)            NOT NULL,
    BIRTH_DATE    DATE                   NOT NULL,
    PHONE_NUMBER  VARCHAR2(20)           NOT NULL,
    ADDRESS       VARCHAR2(500)          NOT NULL,
    LIFE_STATUS   NUMBER(1)    DEFAULT 1 NOT NULL,
    CREATED_BY    VARCHAR2(20) DEFAULT USER,
    CREATED_AT    TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    UPDATED_BY    VARCHAR2(20) DEFAULT USER,
    UPDATED_AT    TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PK_CUSTOMER_INFO PRIMARY KEY (ID),
    CONSTRAINT CHK_CUSTOMER_INFO__LIFE_STATUS CHECK (LIFE_STATUS IN (0, 1))
);

COMMENT ON TABLE CUSTOMER_INFO IS '客户信息表';
COMMENT ON COLUMN CUSTOMER_INFO.ID IS '客户主键ID (自增)';
COMMENT ON COLUMN CUSTOMER_INFO.CUSTOMER_NAME IS '客户姓名';
COMMENT ON COLUMN CUSTOMER_INFO.GENDER IS '性别:M-男;F-女';
COMMENT ON COLUMN CUSTOMER_INFO.BIRTH_DATE IS '出生日期';
COMMENT ON COLUMN CUSTOMER_INFO.PHONE_NUMBER IS '联系电话';
COMMENT ON COLUMN CUSTOMER_INFO.ADDRESS IS '居住地址';
COMMENT ON COLUMN CUSTOMER_INFO.LIFE_STATUS IS '生存状态: 1-生存, 0-死亡';
COMMENT ON COLUMN CUSTOMER_INFO.CREATED_BY IS '创建人';
COMMENT ON COLUMN CUSTOMER_INFO.CREATED_AT IS '创建时间';
COMMENT ON COLUMN CUSTOMER_INFO.UPDATED_BY IS '更新人';
COMMENT ON COLUMN CUSTOMER_INFO.UPDATED_AT IS '更新时间';
